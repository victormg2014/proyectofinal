CREATE DATABASE usuarios;

CREATE TABLE cuenta (id INT NOT NULL PRIMARY KEY AUTO_INCREMENT, usuario VARCHAR(200), nombre VARCHAR(200), clave VARCHAR(200), ruta_foto VARCHAR(200));

CREATE TABLE publicaciones (id INT NOT NULL PRIMARY KEY AUTO_INCREMENT, usuario VARCHAR(200), ruta VARCHAR(200), contenido VARCHAR(200));

CREATE TABLE solicitudes (id INT NOT NULL PRIMARY KEY AUTO_INCREMENT, usuario VARCHAR(200), destino VARCHAR(200), respuesta VARCHAR(200));

CREATE TABLE amigos (id INT NOT NULL PRIMARY KEY AUTO_INCREMENT, usuario VARCHAR(200), usuario2 VARCHAR(200));

CREATE TABLE chat (id INT NOT NULL PRIMARY KEY AUTO_INCREMENT, usuario VARCHAR(200), destino VARCHAR(200), mensaje VARCHAR(200), fecha TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP);

CREATE TABLE comentarios (id INT NOT NULL PRIMARY KEY AUTO_INCREMENT, id_publicacion VARCHAR(200), texto VARCHAR(200), usuario VARCHAR(200), fecha TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP);

CREATE TABLE usuarios_online (id INT NOT NULL PRIMARY KEY AUTO_INCREMENT, fecha INT NOT NULL, usuario VARCHAR(200) NOT NULL );